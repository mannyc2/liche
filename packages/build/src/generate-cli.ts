import type { OperationIR, ProgramIR } from './ir.js'

export type GenerateOptions = {
  generatorVersion: string
  canonicalIrDigest: string
  generationOptionsDigest: string
  surfaceId?: string
}

export function generateCli(ir: ProgramIR, options: GenerateOptions): string {
  const lines: string[] = []
  lines.push(...renderHeader(ir, options))
  lines.push('')
  lines.push(...renderImports(ir))
  lines.push('')
  lines.push(...renderProgram(ir))
  lines.push('')
  return lines.join('\n')
}

function renderHeader(ir: ProgramIR, options: GenerateOptions): string[] {
  return [
    '/**',
    ` * @generated by @lili/build ${options.generatorVersion}`,
    ` * schema: ${ir.name}@${ir.version}`,
    ` * canonical-ir-digest: ${options.canonicalIrDigest}`,
    ` * generation-options-digest: ${options.generationOptionsDigest}`,
    ` * surface-id: ${options.surfaceId ?? 'cli'}`,
    ' * source: canonical-ir',
    ' *',
    ' * Do not edit by hand. Regenerate via `li-build generate`.',
    ' */',
  ]
}

function renderImports(ir: ProgramIR): string[] {
  const out: string[] = []
  out.push(`import { Cli, z } from '@lili/core'`)
  const byModule = new Map<string, Set<string>>()
  for (const op of ir.operations) {
    if (!op.local) continue
    const list = byModule.get(op.local.module) ?? new Set<string>()
    list.add(op.local.export)
    byModule.set(op.local.module, list)
  }
  for (const [module, names] of [...byModule].sort(([a], [b]) => (a < b ? -1 : a > b ? 1 : 0))) {
    const sorted = [...names].sort().join(', ')
    out.push(`import { ${sorted} } from '${rewriteToJs(module)}'`)
  }
  return out
}

function rewriteToJs(module: string): string {
  return module.endsWith('.ts') ? `${module.slice(0, -3)}.js` : module
}

function renderProgram(ir: ProgramIR): string[] {
  const groups = groupByFirstSegment(ir.operations)
  const subCliVars: string[] = []
  const lines: string[] = []

  for (const [groupKey, ops] of groups) {
    if (ops.length === 1 && ops[0]!.command.length === 1) continue
    const varName = sanitizeIdent(groupKey)
    subCliVars.push(varName)
    lines.push(`const ${varName} = Cli.create(${q(groupKey)})`)
    for (const op of ops) {
      const sub = op.command[op.command.length - 1]!
      lines.push(...renderCommandCall('  ', sub, op))
    }
    lines.push('')
  }

  const chain: string[] = []
  for (const [groupKey, ops] of groups) {
    if (ops.length === 1 && ops[0]!.command.length === 1) {
      chain.push(...renderCommandCall('  ', ops[0]!.command[0]!, ops[0]!))
    } else {
      chain.push(`  .command(${sanitizeIdent(groupKey)})`)
    }
  }

  lines.push(`const program = Cli.create({`)
  lines.push(`  name: ${q(ir.name)},`)
  lines.push(`  version: ${q(ir.version)},`)
  lines.push(`  generated: { machineOutput: 'envelope', disabledGlobals: ['format'] },`)
  lines.push(`})`)
  lines.push(...chain)
  lines.push('')
  lines.push(`export default program`)
  void subCliVars
  return lines
}

function groupByFirstSegment(operations: OperationIR[]): Map<string, OperationIR[]> {
  const groups = new Map<string, OperationIR[]>()
  for (const op of operations) {
    const key = op.command[0]!
    const bucket = groups.get(key) ?? []
    bucket.push(op)
    groups.set(key, bucket)
  }
  return groups
}

function renderCommandCall(indent: string, name: string, op: OperationIR): string[] {
  const lines: string[] = []
  const multimode = op.locality.modes.length > 1
  const inputSchema = multimode ? withLocalityFlags(op.input.jsonSchema) : op.input.jsonSchema
  lines.push(`${indent}.command(${q(name)}, {`)
  if (op.description) lines.push(`${indent}  description: ${q(op.description)},`)
  lines.push(`${indent}  options: ${renderSchema(inputSchema, `${indent}  `)},`)
  lines.push(`${indent}  output: ${renderSchema(op.output.jsonSchema, `${indent}  `)},`)
  lines.push(`${indent}  async run(ctx) {`)
  if (!op.local) {
    throw new Error(`Operation '${op.id}' has no local implementation; remote-only generation is Phase 4`)
  }
  if (multimode) {
    lines.push(`${indent}    if (ctx.options.local === true && ctx.options.remote === true) {`)
    lines.push(`${indent}      return ctx.error({ code: 'LOCALITY_CONFLICT', message: '--local and --remote are mutually exclusive' })`)
    lines.push(`${indent}    }`)
    lines.push(`${indent}    let mode = ${q(op.locality.default)}`)
    lines.push(`${indent}    let source = 'schema-default'`)
    lines.push(`${indent}    if (ctx.options.local === true) { mode = 'local'; source = 'flag' }`)
    lines.push(`${indent}    else if (ctx.options.remote === true) {`)
    if (op.remote) {
      lines.push(`${indent}      mode = 'remote'; source = 'flag'`)
      lines.push(`${indent}      return ctx.error({ code: 'REMOTE_NOT_IMPLEMENTED', message: 'Remote transport is not implemented yet (Phase 4)' })`)
    } else {
      lines.push(`${indent}      return ctx.error({ code: 'REMOTE_NOT_IMPLEMENTED', message: 'Remote transport is not implemented yet (Phase 4)' })`)
    }
    lines.push(`${indent}    }`)
    lines.push(`${indent}    const { local: _local, remote: _remote, ...input } = ctx.options`)
    lines.push(`${indent}    const data = await ${op.local.export}(input)`)
    lines.push(`${indent}    return ctx.ok(data, { locality: { mode, source } })`)
  } else {
    lines.push(`${indent}    const data = await ${op.local.export}(ctx.options)`)
    lines.push(`${indent}    return ctx.ok(data, { locality: { mode: ${q(op.locality.default)}, source: 'schema-default' } })`)
  }
  lines.push(`${indent}  },`)
  lines.push(`${indent}})`)
  return lines
}

// Add optional --local and --remote control flags to the input JSON Schema for
// multimode operations. Locality is the only place control flags get layered
// into the operation's options schema; single-mode ops never advertise these.
function withLocalityFlags(jsonSchema: unknown): unknown {
  const root = jsonSchema as JsonSchemaNode
  if (root.type !== 'object') {
    throw new Error('Multimode operations require an object-typed input schema')
  }
  return {
    ...root,
    properties: {
      ...(root.properties ?? {}),
      local: { type: 'boolean' as const },
      remote: { type: 'boolean' as const },
    },
  }
}

function renderSchema(jsonSchema: unknown, indent: string): string {
  return renderSchemaNode(jsonSchema as JsonSchemaNode, indent)
}

type JsonSchemaNode = {
  $schema?: string
  type?: string
  properties?: Record<string, JsonSchemaNode>
  required?: string[]
  items?: JsonSchemaNode
  enum?: unknown[]
  default?: unknown
  additionalProperties?: boolean
}

function renderSchemaNode(node: JsonSchemaNode, indent: string): string {
  if (node.enum && Array.isArray(node.enum)) {
    const literals = node.enum.map((v) => JSON.stringify(v)).join(', ')
    return `z.enum([${literals}])`
  }
  switch (node.type) {
    case 'string':
      return 'z.string()'
    case 'number':
    case 'integer':
      return 'z.number()'
    case 'boolean':
      return 'z.boolean()'
    case 'array':
      if (!node.items) throw new Error('Array schema missing items')
      return `z.array(${renderSchemaNode(node.items, indent)})`
    case 'object': {
      const props = node.properties ?? {}
      const required = new Set(node.required ?? [])
      const keys = Object.keys(props).sort()
      if (keys.length === 0) return 'z.object({})'
      const inner = keys.map((key) => {
        const child = props[key]!
        let expr = renderSchemaNode(child, `${indent}  `)
        if (child.default !== undefined) {
          expr += `.default(${JSON.stringify(child.default)})`
        } else if (!required.has(key)) {
          expr += '.optional()'
        }
        return `${indent}  ${q(key)}: ${expr},`
      })
      return `z.object({\n${inner.join('\n')}\n${indent}})`
    }
    default:
      throw new Error(
        `Unsupported JSON Schema for Phase 3 generator (type=${node.type}): ${JSON.stringify(node).slice(0, 200)}`,
      )
  }
}

function q(s: string): string {
  return `'${s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`
}

function sanitizeIdent(name: string): string {
  const camel = name.replace(/[-_](\w)/g, (_, c: string) => c.toUpperCase())
  if (/^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(camel)) return camel
  throw new Error(`Cannot derive identifier from group name '${name}'`)
}
